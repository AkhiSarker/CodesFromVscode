package datatype;

public class LoopDemo {
    public static void main(String[] args) {
        
        int i=1;
        
        while(i<=10){
            System.out.println(i+" Bangladesh");
            i++;
        }
        
        do{
            System.out.println(i+" Akhi");
            i++;
        }while(i<=20);
                
    }
    
}
