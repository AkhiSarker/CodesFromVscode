package datatype;

public class ForLoop {
    public static void main(String[] args) {
        
        for(int i=1; i<=10; i++)
        {
            System.out.println(i+ " Bangladesh");
        }
        for(int i=2; i<=20; i = i+2)
        {
            System.out.println(i);
        }
        for(int i=10; i>=1; i--)
        {
            System.out.println(i);
        }
    }
    
}
