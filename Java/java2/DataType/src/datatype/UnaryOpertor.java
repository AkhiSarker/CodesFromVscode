package datatype;


public class UnaryOpertor {
    public static void main(String[] args) {
        
        int x = 10;
        int result;
        
        result = +x; //-10
        System.out.println("result = "+result);
        
        result = -x; //10
        System.out.println("result = "+result);
 
        
    }
    
}
